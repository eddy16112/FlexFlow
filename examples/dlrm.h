#include "model.h"
#define MAX_NUM_SAMPLES 128

using namespace Legion;

struct DLRMConfig {
  DLRMConfig(void)
  : sparse_feature_size(2), sigmoid_bot(-1), sigmoid_top(-1),
    loss_threshold(0.0f),
    arch_interaction_op("cat"), dataset_path("") {
    embedding_size.push_back(4);
    //embedding_size.push_back(4);
    //embedding_size.push_back(4);
    mlp_bot.push_back(4);
    mlp_bot.push_back(2);
    mlp_top.push_back(8);
    mlp_top.push_back(2);
  }
  int sparse_feature_size, sigmoid_bot, sigmoid_top;
  float loss_threshold;
  std::vector<int> embedding_size, mlp_bot, mlp_top;
  std::string arch_interaction_op, dataset_path;
};

class DataLoader {
public:
  DataLoader(FFModel& ff, const DLRMConfig& dlrm,
             const std::vector<Tensor>& _sparse_inputs,
             Tensor _dense_input, Tensor _label);
  void load_next_batch(FFModel& ff);
  void shuffle();
  static void load_sparse_input(const Task *task,
                                const std::vector<PhysicalRegion> &regions,
                                Context ctx,
                                Runtime* runtime);
  static void load_dense_input(const Task *task,
                               const std::vector<PhysicalRegion> &regions,
                               Context ctx,
                               Runtime* runtime);
  static void load_label(const Task *task,
                         const std::vector<PhysicalRegion> &regions,
                         Context ctx,
                         Runtime* runtime);
private:
  std::vector<Tensor> full_sparse_inputs, batch_sparse_inputs;
  Tensor full_dense_input, batch_dense_input, full_label, batch_label;
};

struct SampleIdxs {
  int numSamples;
  int idxs[MAX_NUM_SAMPLES];
};

